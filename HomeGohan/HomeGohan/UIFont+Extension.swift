extension UIFont {
    class func mainJaFont(size: CGFloat) -> UIFont {
        return UIFont(name: "HirakakuProN-W3", size: size)!
    }
    
    class func mainEnFont(size: CGFloat) -> UIFont {
        return UIFont(name: "HirakakuProN-W3", size: size)!
    }
}