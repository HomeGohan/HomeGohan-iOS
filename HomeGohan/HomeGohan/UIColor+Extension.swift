import UIKit
extension UIColor {
    class func mainColor() -> UIColor {
        return UIColor(red: 200, green: 178, blue: 152, alpha: 1.0)
    }
    
    class func textBlack() -> UIColor {
        return UIColor(white: 0.2, alpha: 1.0)
    }
}